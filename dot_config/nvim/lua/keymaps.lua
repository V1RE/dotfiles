-- Normal --
-- Resize with arrows
keymap("n", "<S-Up>", ":resize -2<CR>")
keymap("n", "<S-Down>", ":resize +2<CR>")
keymap("n", "<S-Left>", ":vertical resize -2<CR>")
keymap("n", "<S-Right>", ":vertical resize +2<CR>")

-- Move text up and down
keymap("n", "<A-j>", "<Esc>:m .+1<CR>==gi")
keymap("n", "<A-k>", "<Esc>:m .-2<CR>==gi")

-- Visual --
-- Stay in indent mode
keymap("v", "<", "<gv")
keymap("v", ">", ">gv")

-- Move text up and down
keymap("v", "<A-j>", ":m .+1<CR>==")
keymap("v", "<A-k>", ":m .-2<CR>==")
